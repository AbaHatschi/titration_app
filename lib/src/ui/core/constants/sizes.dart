/// A class containing constant size values used throughout the UI.
/// This helps maintain consistency in spacing, padding, and sizing.
/// 
/// Each constant represents a specific size in logical pixels.
/// For example, `Sizes.p16` represents a size of 16 logical pixels.
/// 
/// These constants can be used in widgets to ensure uniformity.
class Sizes {
  static const double p4 = 4.0;
  static const double p8 = 8.0;
  static const double p12 = 12.0;
  static const double p16 = 16.0;
  static const double p20 = 20.0;
  static const double p24 = 24.0;
  static const double p32 = 32.0;
  static const double p40 = 40.0;
  static const double p48 = 48.0;
  static const double p64 = 64.0;
}
